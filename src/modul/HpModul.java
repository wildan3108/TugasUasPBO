package modul;


public class HpModul {
    
    private int id;
    private String brand;
    private String kapasitas_batrai;
    private String memori;
    private String jenis_OS;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getBrand() {
        return brand;
    }

    public void setBrand(String brand) {
        this.brand = brand;
    }

    public String getKapasitas_batrai() {
        return kapasitas_batrai;
    }

    public void setKapasitas_batrai(String kapasitas_batrai) {
        this.kapasitas_batrai = kapasitas_batrai;
    }

    public String getMemori() {
        return memori;
    }

    public void setMemori(String memori) {
        this.memori = memori;
    }

    public String getJenis_OS() {
        return jenis_OS;
    }

    public void setJenis_OS(String jenis_OS) {
        this.jenis_OS = jenis_OS;
    }

    
}
